import mongoose from "mongoose";

const createEnquiry = new mongoose.Schema(
  {
    name: {
      type: String,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    phone: {
      type: String,
      required: true,
    },
    message: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const Enquiry = mongoose.model("enquiry", createEnquiry);

export default Enquiry;
